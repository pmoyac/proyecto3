    public void iniciarsesion() {
        ArrayList<Usuario> usuarios = new UsuarioDAO().buscarTodas();
        String correo_electronico = this.txt_usuario.getText();
        String contrase침a = this.txt_contra.getText();
        for (Usuario usuario : usuarios) {
            if (usuario.getCorreo().equals(correo_electronico) && usuario.getContra().equals(contrase침a)) {

                PantallaInicio fr = new PantallaInicio();
                this.setVisible(false);
                fr.setVisible(true);
                dispose();
                return;
            }

        }
        JOptionPane.showMessageDialog(this, "correo electronico o contrase침a incorrectos/ Vacios, intente de nuevo", "Error de inicio de sesi칩n", JOptionPane.ERROR_MESSAGE);
    }
    

